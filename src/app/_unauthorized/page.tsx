import { notFound } from "next/navigation";

/**
 * Сюда middleware переписывает (rewrite) защищённые маршруты,
 * на которых нет auth-cookies. Страница немедленно бросает notFound() —
 * пользователь видит 404, как будто маршрут вообще не существует.
 *
 * Это:
 *   1) не выкидывает на /login (как просил пользователь — «404, а не редирект»);
 *   2) не раскрывает существование защищённых маршрутов;
 *   3) предотвращает преждевременную загрузку защищённых компонентов —
 *      Next отдаёт сразу not-found.tsx до RSC-рендера.
 */
export default function Unauthorized(): never {
  notFound();
}
